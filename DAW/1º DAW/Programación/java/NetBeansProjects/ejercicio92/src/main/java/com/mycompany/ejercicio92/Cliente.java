// Clase Cliente, representa un cliente que realiza pedidos
public class Cliente {
    private String nombre; // el nombre del cliente
    private String apellidos; // los apellidos del cliente
    private String nif; //DNI del cliente

    public Cliente(String nombre, String apellidos, String nif) {
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.nif = nif;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    public String getNif() {
        return nif;
    }

    public void setNif(String nif) {
        this.nif = nif;
    }


}